/**
 * utils/sanitize.ts
 * -----------------
 * Helper function to escape user inputs against HTML/XSS injection.
 */
export function sanitizeString(str: string): string {
  if (!str) return "";
  return str
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#x27;")
    .replace(/\//g, "&#x2F;");
}

export default sanitizeString;
